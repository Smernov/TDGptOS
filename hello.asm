; hello.asm - Загрузочный сектор, выводящий "Hello, World!" на экран

[BITS 16]             ; 16-битный режим
[ORG 0x7C00]          ; Адрес загрузки (BIOS загружает сектор по этому адресу)

start:
    mov si, hello_msg ; Загружаем адрес сообщения в регистр SI

print_string:
    lodsb             ; Загружаем следующий символ в AL
    or al, al         ; Проверяем, достигнут ли конец строки
    jz halt           ; Если достигнут, то останавливаемся
    mov ah, 0x0E      ; Функция BIOS для вывода символа
    int 0x10          ; Вызов BIOS для вывода символа на экран
    jmp print_string  ; Переход к следующему символу

halt:
    cli               ; Отключаем прерывания
    hlt               ; Останавливаем процессор

hello_msg db 'Hello, World!', 0  ; Сообщение, 0 - конец строки

times 510-($-$$) db 0 ; Заполняем до 510 байт нулями
dw 0xAA55             ; Завершаем сектор сигнатурой загрузочного сектора
